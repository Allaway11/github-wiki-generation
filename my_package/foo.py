def add(a: int, b: int) -> int:
    """Returns sum of a and b.

    Args:
        a (int)
        b (int)

    Returns:
        int: addition of a and b

    """
    return a + b


def subtract(a: int, b: int):
    """Returns value of b subtracted from a.

    Args:
        a (int)
        b (int)

    Returns:
        int: a minus b
    """
    return a - b
